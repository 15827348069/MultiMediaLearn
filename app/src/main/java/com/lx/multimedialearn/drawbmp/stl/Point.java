package com.lx.multimedialearn.drawbmp.stl;

/**
 * 顶点信息：从模型中解析出来的三维空间的顶点
 *
 * @author lixiao
 * @since 2017-09-04 14:02
 */
public class Point {
    public float x;
    public float y;
    public float z;

    public Point(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
